public class Loops {
    public static void main(String[] args) {
        // using for loop_______________________________________________________
        for (int counter = 0; counter <= 100; counter++) {
        System.out.println("Hi this is " + counter);
        }
        // usingwhile loop_______________________________________________________
        int i = 0;
        while (i < 11) {
            System.out.println(i);
            i++;
        }
        // Using DO while loop________________________________
        int j = 0;
        do {
            System.out.println(j);
            j++;
        } while (j < 11);
    }
}